import json
from difflib import SequenceMatcher
from langchain_ollama import OllamaLLM


QUESTIONS_PATH = "data/evaluation/questions.json"


def load_reference_answers():
    """Load the reference Q&A from the JSON file."""
    with open(QUESTIONS_PATH, "r", encoding="utf-8") as f:
        return json.load(f)


def find_reference_answer(question):
    """
    Find the reference answer for a given question.
    Uses fuzzy matching to handle slight wording differences.
    Returns the reference answer string, or None if not found.
    """
    references = load_reference_answers()

    best_match = None
    best_score = 0.0
    #find the right query in the JSON file
    for entry in references:
        score = SequenceMatcher(
            None,
            question.lower().strip(),
            entry["question"].lower().strip()
        ).ratio()

        if score > best_score:
            best_score = score
            best_match = entry

    # Only accept if similarity is above 60%
    if best_score >= 0.6:
        return best_match
    
    return None

# Rate it on a scale of 0 to 10. And since it’s an LLM, it understands the meaning of the text, not the exact words.
def evaluate_answers(question, agent_answer, reference_answer):
    """
    Use the LLM to compare the agent answer with the reference answer.
    Returns a text report with a score from 0 to 10.
    """
    llm = OllamaLLM(model="mistral")

    prompt = f"""
You are an expert evaluator for IEC 62304 answers.

Your task is to compare two answers to the same question and evaluate how similar they are in terms of content, accuracy, and completeness.

Question:
{question}

Reference answer (written by a human expert based on the standard):
{reference_answer}

Agent answer (generated by an AI assistant):
{agent_answer}

Evaluation instructions:
- Score from 0 to 10 based on content similarity and accuracy
- 0 = completely wrong or unrelated
- 5 = partially correct, missing important points
- 10 = fully correct and complete
- Focus on meaning and content, not exact wording
- Identify what the agent got right, what it missed, and what it got wrong

Write a short evaluation report with this structure:
Score: X/10
What the agent got right: ...
What the agent missed or got wrong: ...
Overall assessment: ...
"""

    return llm.invoke(prompt).strip()


def run_evaluation(question, agent_answer):
    """
    Main evaluation function called by the central agent.
    Looks up the reference answer and runs the evaluation.
    """
    match = find_reference_answer(question)

    if match is None:
        return "This question is not in the evaluation file. No comparison available."

    reference_answer = match["reference_answer"]

    print(f"\n[Evaluating against reference: {match['id']} - {match['question']}]\n")

    report = evaluate_answers(question, agent_answer, reference_answer)

    return report
